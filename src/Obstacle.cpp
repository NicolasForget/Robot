using namespace std;

#include "Obstacle.hpp"

//-----------------------------------------------------------------------
// Constructor :
//-----------------------------------------------------------------------

Obstacle::Obstacle(int htr){
	if(htr > 0){
		_hauteur = htr;
	}
	else{
		throw NegativeHauteurException();
	}
}

//-----------------------------------------------------------------------
// Default Constructor :
//-----------------------------------------------------------------------

Obstacle::Obstacle(){
	_hauteur = 1;
}

//-----------------------------------------------------------------------
// get
//-----------------------------------------------------------------------

int Obstacle::getHauteur(){
	return _hauteur;
}

//-----------------------------------------------------------------------
// Display
//-----------------------------------------------------------------------

ostream& operator<<(ostream& os, Obstacle& obs){
    os << obs.getHauteur() << "cm de haut" << endl;
    return os;
}
