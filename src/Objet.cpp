#include "Objet.hpp"
using namespace std;

std::map<string,Objet*> Objet::objets = {
	{"objet0", new Objet(5)}
};

//-----------------------------------------------------------------------
// Constructor :
//-----------------------------------------------------------------------

Objet::Objet(int pds){
	if(pds >= 0){
		_poids = pds;
	}
	else{
		throw NegativePoidsException();
	}
}

//-----------------------------------------------------------------------
// Default Constructor :
//-----------------------------------------------------------------------

Objet::Objet(){
	_poids = 0;
}

//-----------------------------------------------------------------------
// get
//-----------------------------------------------------------------------

int Objet::getPoids(){
	return _poids;
}

//-----------------------------------------------------------------------
// Display
//-----------------------------------------------------------------------

ostream& operator<<(ostream& os, Objet& obj){
    os << obj.getPoids() << "kg";
    return os;
}


