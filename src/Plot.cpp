using namespace std;

#include "Plot.hpp"

//-----------------------------------------------------------------------
// Constructor :
//-----------------------------------------------------------------------

Plot::Plot(int htr){
	if(htr > 0){
		_hauteur = htr;
	}
	else{
		throw NegativeHauteurException();
	}
}

//-----------------------------------------------------------------------
// Default Constructor :
//-----------------------------------------------------------------------

Plot::Plot(){
	_hauteur = 1;
}

//-----------------------------------------------------------------------
// get
//-----------------------------------------------------------------------

int Plot::getHauteur(){
	return _hauteur;
}

//-----------------------------------------------------------------------
// Display
//-----------------------------------------------------------------------

ostream& operator<<(ostream& os, Plot& obs){
    os << obs.getHauteur() << "cm de haut" << endl;
    return os;
}
